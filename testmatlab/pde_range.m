function pdemodel
[pde_fig,ax]=pdeinit;
pdetool('appl_cb',1);
pdetool('snapon','on');
set(ax,'DataAspectRatio',[1 1 1]);
set(ax,'PlotBoxAspectRatio',[1.5 1 1]);
set(ax,'XLim',[-1.5 1.5]);
set(ax,'YLim',[-1 1]);
set(ax,'XTickMode','auto');
set(ax,'YTickMode','auto');
pdetool('gridon','on');

% Geometry description:
pderect([-0.5 1 0 -0.59999999999999998],'R1');
pdeellip(-0.5,-0.20000000000000007,0.5,0.40000000000000002,...
0,'E1');
pdeellip(0.5,0.20000000000000018,0.5,0.40000000000000013,...
0,'E2');
set(findobj(get(pde_fig,'Children'),'Tag','PDEEval'),'String','(E1+R1)-E2')

% Boundary conditions:
pdetool('changemode',0)
pdesetbd(13,...
'neu',...
1,...
'0',...
'-5')
pdesetbd(12,...
'neu',...
1,...
'0',...
'-5')
pdesetbd(11,...
'neu',...
1,...
'0',...
'-5')
pdesetbd(10,...
'neu',...
1,...
'0',...
'-5')
pdesetbd(7,...
'neu',...
1,...
'0',...
'-5')
pdesetbd(6,...
'neu',...
1,...
'0',...
'-5')
pdesetbd(5,...
'neu',...
1,...
'0',...
'-5')
pdesetbd(2,...
'neu',...
1,...
'0',...
'-5')
pdesetbd(1,...
'neu',...
1,...
'0',...
'-5')

% Mesh generation:
setappdata(pde_fig,'Hgrad',1.3);
setappdata(pde_fig,'refinemethod','regular');
setappdata(pde_fig,'jiggle',char('on','mean',''));
pdetool('initmesh')
pdetool('jiggle')
pdetool('jiggle')
pdetool('jiggle')
pdetool('jiggle')
pdetool('refine')
pdetool('refine')

% PDE coefficients:
pdeseteq(1,...
'1.0',...
'0.0',...
'10.0',...
'1.0',...
'0:10',...
'0.0',...
'0.0',...
'[0 100]')
setappdata(pde_fig,'currparam',...
['1.0 ';...
'0.0 ';...
'10.0';...
'1.0 '])

% Solve parameters:
setappdata(pde_fig,'solveparam',...
str2mat('0','3048','10','pdeadworst',...
'0.5','longest','0','1E-4','','fixed','Inf'))

% Plotflags and user data strings:
setappdata(pde_fig,'plotflags',[1 1 2 1 1 1 1 1 0 0 0 1 0 0 1 0 0 1]);
setappdata(pde_fig,'colstring','');
setappdata(pde_fig,'arrowstring','');
setappdata(pde_fig,'deformstring','');
setappdata(pde_fig,'heightstring','');

% Solve PDE:
pdetool('solve')
